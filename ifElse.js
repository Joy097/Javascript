function testSize(num){
    if(num < 5){
        console.log("tiny");}
    else if(num < 10){
        console.log("small");}
    else if(num < 15){
        console.log("medium");}
    else if(num < 20){
        console.log("large");}
    else if(num < 25){
        console.log("huge");}

}

testSize(5);
testSize(10);